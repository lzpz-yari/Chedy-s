    // Método para realizar la consulta a la base de datos y mostrar los resultados en un JTable
    public void consultar() {
        conet = con1.getConnection();
        String sql = "SELECT RFC, Nombre, Apellido_Paterno, Apellido_Materno, Correo, Salario, Puesto, FechaNacimiento FROM Empleado"; // Agregar FechaNacimiento a la consulta
        try {
            PreparedStatement statement = conet.prepareStatement(sql);
            ResultSet result = statement.executeQuery();

            // Se utiliza DefaultTableModel y fireTableDataChanged() para mejorar la velocidad de actualización
            modelo = (DefaultTableModel) tablaEmpleados.getModel();
            modelo.setRowCount(0);

            while (result.next()) {
                Object[] row = {
                    result.getString("RFC"),
                    result.getString("Nombre"),
                    result.getString("Apellido_Paterno"),
                    result.getString("Apellido_Materno"),
                    result.getString("Correo"),
                    result.getDouble("Salario"),
                    result.getString("Puesto"),
                    result.getDate("FechaNacimiento")
                };
                modelo.addRow(row);
            }

            // Notifica a la JTable que los datos han cambiado
            modelo.fireTableDataChanged();

        } catch (SQLException e) {
            //e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error al consultar la base de datos: " + e.getMessage());
        }
    }

