    private void btModificacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btModificacionActionPerformed
        int filaSeleccionada = tablaEmpleados.getSelectedRow();
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un empleado para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String rfcEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 0).toString(); // Obtener el RFC del empleado
                String nombreEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 1).toString();
                String apellidoPaternoEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 2).toString();
                String apellidoMaternoEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 3).toString();

                String sql = "SELECT Correo, Salario, Puesto FROM Empleado WHERE RFC = ?"; // Consultar usando RFC en lugar de ID
                PreparedStatement statement = conet.prepareStatement(sql);
                statement.setString(1, rfcEmpleado);
                ResultSet result = statement.executeQuery();

                if (result.next()) {
                    String correoEmpleado = result.getString("Correo");
                    double salarioEmpleado = result.getDouble("Salario");
                    String puestoEmpleado = result.getString("Puesto");

                    ModificacionEmpleado modificar = new ModificacionEmpleado(this, tablaEmpleados);
                    modificar.setDatos(rfcEmpleado, nombreEmpleado, apellidoPaternoEmpleado, apellidoMaternoEmpleado, correoEmpleado, salarioEmpleado, puestoEmpleado);
                    modificar.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "No se encontraron datos del empleado en la base de datos.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error al obtener los datos del empleado: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_btModificacionActionPerformed


    private void btBajaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBajaActionPerformed
        // Obtener la fila seleccionada en la tabla de empleados
        int filaSeleccionada = tablaEmpleados.getSelectedRow();

// Verificar si se ha seleccionado una fila válida
        if (filaSeleccionada != -1) {
            // Obtener el RFC del registro seleccionado
            String rfcEmpleadoAEliminar = (String) tablaEmpleados.getValueAt(filaSeleccionada, 0); // RFC es la primera columna

            // Construir un mensaje de confirmación con los datos del empleado seleccionado
            String mensajeConfirmacion = "¿Estás seguro de que deseas eliminar al empleado con RFC:\n" + rfcEmpleadoAEliminar + "?";

            // Mostrar un cuadro de diálogo de confirmación
            int confirmacion = JOptionPane.showConfirmDialog(null, mensajeConfirmacion, "Confirmar eliminación", JOptionPane.YES_NO_OPTION);

            // Si el usuario confirma la eliminación, proceder con la eliminación del registro
            if (confirmacion == JOptionPane.YES_OPTION) {
                // Construir la sentencia SQL para eliminar el registro que coincida con el RFC seleccionado
                String sql = "DELETE FROM Empleado WHERE RFC = ?";

                try {
                    // Crear una conexión a la base de datos
                    Connection conet = con1.getConnection();

                    // Crear una sentencia preparada con la consulta SQL
                    PreparedStatement statement = conet.prepareStatement(sql);

                    // Establecer el parámetro para el RFC del registro que deseas eliminar
                    statement.setString(1, rfcEmpleadoAEliminar);

                    // Ejecutar la sentencia SQL para eliminar el registro
                    int filasAfectadas = statement.executeUpdate();

                    // Verificar si se eliminó el registro exitosamente
                    if (filasAfectadas > 0) {
                        // Eliminar el registro seleccionado de la tabla
                        DefaultTableModel modelo = (DefaultTableModel) tablaEmpleados.getModel();
                        modelo.removeRow(filaSeleccionada);

                        // Mostrar un mensaje de éxito
                        JOptionPane.showMessageDialog(null, "Empleado eliminado correctamente.");
                        //this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "No se pudo eliminar el registro.");
                    }
                } catch (SQLException e) {
                    // Manejar cualquier excepción que ocurra durante la ejecución de la consulta SQL
                    JOptionPane.showMessageDialog(null, "Error al eliminar el registro: " + e.getMessage());
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione un empleado para eliminar.");
        }

    }//GEN-LAST:event_btBajaActionPerformed

