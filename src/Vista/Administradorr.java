    private void AgregarCajeroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AgregarCajeroActionPerformed
        int filaSeleccionada = tablaEmpleados.getSelectedRow();

        if (filaSeleccionada != -1) {
            String idEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 0).toString();  // Obtener el ID del empleado
            String correoEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 4).toString();  // Obtener el correo
            String puestoEmpleado = tablaEmpleados.getValueAt(filaSeleccionada, 6).toString();  // Obtener el puesto

            if (puestoEmpleado.equalsIgnoreCase("Cajero")) {
                Credenciales credenciales = new Credenciales(correoEmpleado, idEmpleado);
                credenciales.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "El usuario seleccionado no es un cajero.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Por favor, seleccione un empleado.");
        }

    }//GEN-LAST:event_AgregarCajeroActionPerformed

